    sections: list[dict[str, str]]  # structured sections
    citations: list[dict[str, str]]  # cited authorities
    client_id: str = ""
    tax_year: int = 2024
    created_at: datetime = field(default_factory=lambda: datetime.now(UTC))
    model_used: str = ""
    confidence: float = 0.0
    word_count: int = 0
    disclaimer: str = ""


